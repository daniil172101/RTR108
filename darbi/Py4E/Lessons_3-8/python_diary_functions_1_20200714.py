print(max('Hello world')) # Function 'max' gives the largest value in a list
print(min('Hello world')) # Function 'min' gives the smallest value in a list
print(len('Hello world')) # Function 'len' returns number of items in argument
print(int('32'))        # Function 'int' takes any value and, if possible, converts it to an integer
print(float(32))        # Function 'float' converts integers and strings to floating-point numbers
print(str(57))          # Function 'str' takes any value and, if possible, converts it to a string
